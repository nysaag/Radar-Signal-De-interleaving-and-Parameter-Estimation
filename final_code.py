def merge_close_pulses(
    pulses,
    max_gap_samples=10
):
    """
    Merge pulse fragments that are
    separated by only a few samples.
    """

    if len(pulses) == 0:
        return []

    merged = []

    current = pulses[0].copy()

    for p in pulses[1:]:

        gap = (
            p["start"]
            -
            current["end"]
        )

        if gap <= max_gap_samples:

            current["end"] = p["end"]

            current["energy"] += p["energy"]

            if p["energy"] > current["energy"]:
                current["peak"] = p["peak"]

        else:

            merged.append(current)

            current = p.copy()

    merged.append(current)

    return merged

