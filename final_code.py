def initial_rf_groups(
    pdws,
    gap_hz=50000
):

    pdws = sorted(
        pdws,
        key=lambda p:
        p["frequency"]
    )

    groups = []

    current = [pdws[0]]

    for i in range(
        1,
        len(pdws)
    ):

        diff = abs(
            pdws[i]["frequency"]
            -
            pdws[i-1]["frequency"]
        )

        if diff < gap_hz:

            current.append(
                pdws[i]
            )

        else:

            groups.append(
                current
            )

            current = [pdws[i]]

    groups.append(
        current
    )

    return groups

