def extract_pulses(
    clean_mask,
    power,
    fs=FS
):

    pulses = []

    inside = False

    start = 0

    N = len(clean_mask)

    for i in range(N):

        if clean_mask[i] and not inside:

            start = i
            inside = True

        elif not clean_mask[i] and inside:

            end = i

            width = end - start

            peak_idx = (
                start
                +
                np.argmax(
                    power[start:end]
                )
            )

            energy = np.sum(
                power[start:end]
            )

            pulses.append({

                "start": start,
                "end": end,
                "peak": peak_idx,
                "toa": peak_idx / fs,

                "width_samples": width,
                "width_sec": width / fs,

                "energy": energy
            })

            inside = False

    return pulses

