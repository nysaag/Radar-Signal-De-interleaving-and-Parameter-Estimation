def estimate_track_pri(
    track
):

    toas = np.array([

        p["toa"]

        for p in track["pulses"]

    ])

    toas = np.sort(
        toas
    )

    if len(toas) < 3:

        return None

    intervals = np.diff(
        toas
    )

    pri = np.median(
        intervals
    )

    return pri

