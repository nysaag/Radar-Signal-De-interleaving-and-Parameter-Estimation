def compute_spatial_signature(
    data,
    pulse
):

    start = pulse["start"]
    end = pulse["end"]

    snapshot = data[:, start:end]

    if snapshot.shape[1] < 4:

        return [0.0, 0.0, 0.0]

    phases = []

    for k in range(1, 4):

        r = np.mean(
            snapshot[k] *
            np.conj(snapshot[k-1])
        )

        phases.append(
            np.angle(r)
        )

    return phases

def add_spatial_features(
    data,
    pulses
):

    for pulse in pulses:

        sig = compute_spatial_signature(
            data,
            pulse
        )

        pulse["phase01"] = sig[0]
        pulse["phase12"] = sig[1]
        pulse["phase23"] = sig[2]

